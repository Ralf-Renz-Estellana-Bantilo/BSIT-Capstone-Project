	formatName = (name) => {
		let holdOutput = "";
		let nameLength = name.length;
		let isSpace = false;
		let holdIndex = 0;
		let b;

		for (let a = 0; a < nameLength; a++) {
			if (a === 0) {
				b = name[a].toUpperCase();
				holdOutput += b;
			} else if (a !== nameLength) {
				b = name[a];
				if (b === " ") {
					isSpace = true;
					holdIndex = a + 1;
					holdOutput += b;
				}
				if (isSpace === true) {
					if (a === holdIndex) {
						b = name[a].toUpperCase();
						holdOutput += b;
						isSpace = false;
					}
				} else {
					b = name[a].toLowerCase();
					holdOutput += b;
				}
			}
		}

		return holdOutput;
	};
